package Demo;

//基本数据类型
public class VariableDemo2 {
    public static void main(String[] args){
        //byte -128---127
        byte b = 10;
        System.out.println(b);
        //long
        //long类型后面要加一个L
        long l = 99999999L;
        System.out.println(l);
        //float double>float>long>int>short>byte
        //float类型的变量需要在后面加一个F
        float f = 3.1415F;
        System.out.println(f);
        //char
        char c ='中';
        System.out.println(c);

    }
}
